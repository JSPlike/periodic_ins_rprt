package com.api.reporting.dto;

public class RegistStatusDTO {

	private int code;
	
	private String code_hex;
	
	private String code_nm;
	
	private String code_message;

	public int getCode() {
		return code;
	}

	public void setCode(int code) {
		this.code = code;
	}

	public String getCode_hex() {
		return code_hex;
	}

	public void setCode_hex(String code_hex) {
		this.code_hex = code_hex;
	}

	public String getCode_nm() {
		return code_nm;
	}

	public void setCode_nm(String code_nm) {
		this.code_nm = code_nm;
	}

	public String getCode_message() {
		return code_message;
	}

	public void setCode_message(String code_message) {
		this.code_message = code_message;
	}
	
	
	
}

