package com.api.reporting.cmn.dto;

public class MenuDTO {

	private String menu_id;
	private String menu_nm;
	private int menu_lvl;
	private int menu_ord;
	private String menu_url;
	private String menu_file;
	
	public String getMenu_id() {
		return menu_id;
	}
	public void setMenu_id(String menu_id) {
		this.menu_id = menu_id;
	}
	public String getMenu_nm() {
		return menu_nm;
	}
	public void setMenu_nm(String menu_nm) {
		this.menu_nm = menu_nm;
	}
	public int getMenu_lvl() {
		return menu_lvl;
	}
	public void setMenu_lvl(int menu_lvl) {
		this.menu_lvl = menu_lvl;
	}
	public int getMenu_ord() {
		return menu_ord;
	}
	public void setMenu_ord(int menu_ord) {
		this.menu_ord = menu_ord;
	}
	public String getMenu_url() {
		return menu_url;
	}
	public void setMenu_url(String menu_url) {
		this.menu_url = menu_url;
	}
	public String getMenu_file() {
		return menu_file;
	}
	public void setMenu_file(String menu_file) {
		this.menu_file = menu_file;
	}

	
	
}
