package com.api.reporting.cmn.dto;

public class ModuleDTO {

	private String module_serial;
	
	private String site_id;
	
	private String serial_name;
	
	private String detail;
	
	private String module_variety;
	
	private String variety_name;

	private String site_name;

	public String getModule_serial() {
		return module_serial;
	}

	public void setModule_serial(String module_serial) {
		this.module_serial = module_serial;
	}

	public String getSite_id() {
		return site_id;
	}

	public void setSite_id(String site_id) {
		this.site_id = site_id;
	}

	public String getSerial_name() {
		return serial_name;
	}

	public void setSerial_name(String serial_name) {
		this.serial_name = serial_name;
	}

	public String getDetail() {
		return detail;
	}

	public void setDetail(String detail) {
		this.detail = detail;
	}

	public String getModule_variety() {
		return module_variety;
	}

	public void setModule_variety(String module_variety) {
		this.module_variety = module_variety;
	}

	public String getVariety_name() {
		return variety_name;
	}

	public void setVariety_name(String variety_name) {
		this.variety_name = variety_name;
	}

	public String getSite_name() {
		return site_name;
	}

	public void setSite_name(String site_name) {
		this.site_name = site_name;
	}
}
