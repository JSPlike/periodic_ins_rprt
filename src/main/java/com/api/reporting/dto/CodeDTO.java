package com.api.reporting.dto;

public class CodeDTO {
	
	private String code;
	
	private String code_master;
	
	private String code_detail;
	
	private String code_name;
	
	private String code_name_kr;

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}
	
	public String getCode_master() {
		return code_master;
	}

	public void setCode_master(String code_master) {
		this.code_master = code_master;
	}

	public String getCode_detail() {
		return code_detail;
	}

	public void setCode_detail(String code_detail) {
		this.code_detail = code_detail;
	}

	public String getCode_name() {
		return code_name;
	}

	public void setCode_name(String code_name) {
		this.code_name = code_name;
	}

	public String getCode_name_kr() {
		return code_name_kr;
	}

	public void setCode_name_kr(String code_name_kr) {
		this.code_name_kr = code_name_kr;
	}
	

}
